# String
print("**string**")
name="silpa"
print(type(name))

# Integer
print("**Integer**")
age= 25
print(type(age))

# List
print("**List**")
names=["silpa","mohan",24,2.5]
print(names)
print(type(names))

# Dictionary
print("**Dictionary**")
names= {"name1":"silpa","name2":"mohan"}
print(names)
print(type(names))

# Tuple
print("**Tuple**")
names= ('silpa','mohan')
print(names)
print(type(names))


# Set
print("**Set**")
names= {'silpa','mohan'}
print(names)
print(type(names))
